    private void recargar() {
        try {

            l = new Lienzo(cod_bus);
            panel.removeAll();
            panel.add(l, BorderLayout.CENTER);
            validate();

        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Error", 0);
        }
    }

    private void buscar() {
        try {

            int bus = Integer.parseInt(JOptionPane.showInputDialog(this, "Escriba el codigo de la Persona", "Buscar", 3));
            cod_bus = bus;
            recargar();
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Error", 0);
        }
    }

